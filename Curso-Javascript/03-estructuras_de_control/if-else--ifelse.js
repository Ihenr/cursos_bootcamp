// if else + ifelse

const nota = 5;

if(nota === 5){
    console.log("Sobresaliente");
} else if(nota === 4){
    console.log("MuyBueno");
}else if (nota === 3){
    console.log("Bueno");
}else if (nota === 2){
    console.log("Regular");
}else if(nota === 1){
    console.log("Mal");
}else if(nota === 0){
    console.log("Pesimo");
}else{
    console.log("Error, introduce una nota del 1 al 5");
}